"""
Conversational Planner — LangGraph graph implementation.

Graph topology:
    START → plan_node → gather_node → propose_node
                             ↑              |
                             └──────────────┘  (needs_more_data and gather_iterations < 1)
                                           ↓
                                   human_review_node → execute_node → END
"""
from __future__ import annotations

import asyncio
import json
import uuid
from datetime import date, timedelta
from typing import Any

import aiosqlite
from langchain_core.messages import AIMessage, HumanMessage, SystemMessage
from langchain_google_genai import ChatGoogleGenerativeAI
from langgraph.checkpoint.memory import MemorySaver
from langgraph.graph import END, START, StateGraph
from langgraph.types import interrupt
from typing_extensions import TypedDict

from ..config import GEMINI_API_KEY, GEMINI_MODEL, PLAN_MD_PATH
from ..db.connection import get_db
from ..db.queries import insert_event, reschedule_task, upsert_system_state
from ..tools.planner_tools import (
    check_capacity,
    get_current_plan,
    get_resource_progress,
    get_task_stats,
    get_tasks_by_date,
    rewrite_plan,
    update_tasks,
)

# ---------------------------------------------------------------------------
# State
# ---------------------------------------------------------------------------


class PlannerState(TypedDict):
    messages: list               # Full conversation history (HumanMessage / AIMessage)
    user_input: str
    intent: str                  # Intent description parsed by plan_node
    tools_needed: list[str]      # Read tools that plan_node decided to call
    gathered_data: dict          # Data collected by gather_node
    gather_iterations: int       # Back-track counter; capped at 1
    proposal: dict | None        # Change proposal generated by propose_node
    needs_more_data: bool        # Whether propose_node wants to gather more
    user_confirmed: bool | None
    pending_changes: list        # Write operations to be executed


# ---------------------------------------------------------------------------
# LLM factory
# ---------------------------------------------------------------------------

def _llm() -> ChatGoogleGenerativeAI:
    return ChatGoogleGenerativeAI(
        model=GEMINI_MODEL,
        google_api_key=GEMINI_API_KEY,
        temperature=0.2,
    )


