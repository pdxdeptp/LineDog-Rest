async def plan_node(state: PlannerState) -> dict:
    """Parse user intent and decide which read tools to call."""
    llm = _llm()

    history = list(state.get("messages", []))
    history.append(HumanMessage(content=state["user_input"]))

    today = date.today().isoformat()
    week_end = (date.today() + timedelta(days=6)).isoformat()

    system_content = _PLAN_SYSTEM_PROMPT + f"\n\n今天的日期是 {today}，本周结束日期是 {week_end}。"

    response = await llm.ainvoke(
        [SystemMessage(content=system_content)] + history
    )

    raw = response.content.strip()
    # Strip markdown code fences if present
    if raw.startswith("```"):
        raw = raw.split("```")[1]
        if raw.startswith("json"):
            raw = raw[4:]
    raw = raw.strip()

    try:
        parsed = json.loads(raw)
    except json.JSONDecodeError:
        parsed = {
            "intent": raw,
            "tools_needed": ["get_current_plan", "get_task_stats"],
            "tool_params": {"get_task_stats": {"period": "today"}},
        }

    updated_messages = list(state.get("messages", []))
    updated_messages.append(HumanMessage(content=state["user_input"]))

    return {
        "messages": updated_messages,
        "intent": parsed.get("intent", ""),
        "tools_needed": parsed.get("tools_needed", []),
        # Stash tool params temporarily in gathered_data as "__params__"
        "gathered_data": {"__params__": parsed.get("tool_params", {})},
        "gather_iterations": state.get("gather_iterations", 0),
    }


async def gather_node(state: PlannerState) -> dict:
    """Call required read tools in parallel and merge results."""
    tools_needed: list[str] = state.get("tools_needed", [])
    existing_data: dict = dict(state.get("gathered_data", {}))
    params: dict = existing_data.pop("__params__", {})

    today = date.today()
    week_end = (today + timedelta(days=6)).isoformat()
    today_str = today.isoformat()

    async def _call_tool(name: str) -> tuple[str, Any]:
        async with get_db() as db:
            if name == "get_tasks_by_date":
                p = params.get("get_tasks_by_date", {})
                result = await get_tasks_by_date(db, p.get("date", today_str))
            elif name == "get_current_plan":
                result = await get_current_plan(db, PLAN_MD_PATH)
            elif name == "get_task_stats":
                p = params.get("get_task_stats", {})
                result = await get_task_stats(db, p.get("period", "today"))
            elif name == "get_resource_progress":
                p = params.get("get_resource_progress", {})
                rid = p.get("resource_id")
                if rid is None:
                    result = {"error": "resource_id not provided"}
                else:
                    result = await get_resource_progress(db, int(rid))
            elif name == "check_capacity":
                p = params.get("check_capacity", {})
                result = await check_capacity(
                    db,
                    p.get("start", today_str),
                    p.get("end", week_end),
                )
            else:
                result = {"error": f"unknown tool: {name}"}
        return name, result

    if tools_needed:
        results = await asyncio.gather(*[_call_tool(t) for t in tools_needed])
        gathered: dict = {name: data for name, data in results}
    else:
        gathered = {}

    gathered.update(existing_data)

    return {
        "gathered_data": gathered,
        "gather_iterations": state.get("gather_iterations", 0) + 1,
    }


async def propose_node(state: PlannerState) -> dict:
    """Generate a change proposal based on gathered data and user intent."""
    llm = _llm()

    today = date.today().isoformat()
    context = {
        "today": today,
        "intent": state.get("intent", ""),
        "gathered_data": state.get("gathered_data", {}),
    }

    history = list(state.get("messages", []))
    context_msg = HumanMessage(
        content=f"[系统上下文]\n{json.dumps(context, ensure_ascii=False, indent=2)}"
    )

    response = await llm.ainvoke(
        [SystemMessage(content=_PROPOSE_SYSTEM_PROMPT)] + history + [context_msg]
    )

    raw = response.content.strip()
    if raw.startswith("```"):
        raw = raw.split("```")[1]
        if raw.startswith("json"):
            raw = raw[4:]
    raw = raw.strip()

    try:
        parsed = json.loads(raw)
    except json.JSONDecodeError:
        parsed = {
            "description": raw,
            "changes": [],
            "affects_deadline": False,
            "summary_for_user": raw,
            "needs_more_data": False,
        }

    needs_more = bool(parsed.get("needs_more_data", False))
    # Force-stop infinite gather loop
    if state.get("gather_iterations", 0) >= 1:
        needs_more = False

    proposal = {
        "description": parsed.get("description", ""),
        "changes": parsed.get("changes", []),
        "affects_deadline": parsed.get("affects_deadline", False),
        "summary_for_user": parsed.get("summary_for_user", parsed.get("description", "")),
    }

    # Pre-compute pending_changes via update_tasks (dry run)
    pending: list = []
    if proposal["changes"]:
        async with get_db() as db:
            result = await update_tasks(db, proposal["changes"])
            pending = result.get("pendingChanges", [])

    return {
        "proposal": proposal,
        "needs_more_data": needs_more,
        "pending_changes": pending,
    }


def route_after_propose(state: PlannerState) -> str:
    """Route back to gather if more data is needed (max 1 extra iteration)."""
    if state.get("needs_more_data") and state.get("gather_iterations", 0) < 1:
        return "gather"
    proposal = state.get("proposal") or {}
    if not proposal.get("changes"):
        return "respond"
    return "human_review"


async def respond_node(state: PlannerState) -> dict:
    """Return a conversational text reply when there are no changes to confirm."""
    proposal = state.get("proposal") or {}
    text = proposal.get("summary_for_user") or proposal.get("description") or "好的，已了解。"
    msg = AIMessage(content=text)
    return {"messages": state.get("messages", []) + [msg]}


async def human_review_node(state: PlannerState) -> dict:
    """Gate node: graph pauses BEFORE this node via interrupt_before=["human_review"].
    When confirm_proposal() resumes with {"user_confirmed": bool}, that value is
    already applied to state before this node runs — nothing to do here."""
    return {}


async def execute_node(state: PlannerState) -> dict:
    """Execute approved changes or return a cancellation message."""
    if not state.get("user_confirmed"):
        cancel_msg = AIMessage(content="好的，已取消变更，保持现有计划不变。")
        return {"messages": state.get("messages", []) + [cancel_msg]}

    pending: list = state.get("pending_changes", [])
    applied: list[dict] = []

    plan_content_override: str | None = None

    async with get_db() as db:
        for change in pending:
            action = change.get("action")
            task_id = change.get("task_id")

            if action == "reschedule" and task_id:
                new_date_str = change.get("scheduled_date", "")
                try:
                    new_date = date.fromisoformat(new_date_str)
                    await reschedule_task(db, int(task_id), new_date)
                    applied.append(change)
                except (ValueError, TypeError):
                    pass

            elif action == "update" and task_id:
                fields: list[str] = []
                values: list[Any] = []
                if "priority" in change:
                    fields.append("priority = ?")
                    values.append(change["priority"])
                if "scheduled_date" in change:
                    fields.append("scheduled_date = ?")
                    values.append(change["scheduled_date"])
                if fields:
                    values.append(int(task_id))
                    await db.execute(
                        f"UPDATE tasks SET {', '.join(fields)} WHERE id = ?",
                        values,
                    )
                    await db.commit()
                    applied.append(change)

        proposal = state.get("proposal") or {}
        if plan_content_override:
            await rewrite_plan(db, PLAN_MD_PATH, plan_content_override)

        await insert_event(db, "plan_updated", {
            "triggered_by": "conversational_planner",
            "changes_applied": len(applied),
            "intent": state.get("intent", ""),
        })

    summary = proposal.get("summary_for_user", f"已应用 {len(applied)} 项变更。")
    done_msg = AIMessage(content=f"{summary}\n\n（共应用 {len(applied)} 项变更）")

    return {"messages": state.get("messages", []) + [done_msg]}


# ---------------------------------------------------------------------------
# Graph assembly
# ---------------------------------------------------------------------------

def create_planner_graph():
    """Build and compile the conversational planner graph with MemorySaver."""
    builder = StateGraph(PlannerState)

    builder.add_node("plan", plan_node)
    builder.add_node("gather", gather_node)
    builder.add_node("propose", propose_node)
    builder.add_node("respond", respond_node)
    builder.add_node("human_review", human_review_node)
    builder.add_node("execute", execute_node)

    builder.add_edge(START, "plan")
    builder.add_edge("plan", "gather")
    builder.add_edge("gather", "propose")
    builder.add_conditional_edges(
        "propose",
        route_after_propose,
        {"gather": "gather", "respond": "respond", "human_review": "human_review"},
    )
    builder.add_edge("respond", END)
    builder.add_edge("human_review", "execute")
    builder.add_edge("execute", END)

    memory = MemorySaver()
    return builder.compile(checkpointer=memory, interrupt_before=["human_review"])


# Singleton graph (compiled once)
_graph = None


def _get_graph():
    global _graph
    if _graph is None:
        _graph = create_planner_graph()
    return _graph


