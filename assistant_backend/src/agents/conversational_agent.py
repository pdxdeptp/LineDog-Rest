async def start_conversation(user_input: str, thread_id: str | None = None) -> dict:
    """Start or continue a conversation thread.

    Returns:
        {
            "thread_id": str,
            "proposal": dict | None,   # present when the graph reaches human_review
            "response": str | None,    # present for non-proposal replies
        }
    """
    if thread_id is None:
        thread_id = str(uuid.uuid4())

    graph = _get_graph()
    config = {"configurable": {"thread_id": thread_id}}

    initial_state: PlannerState = {
        "messages": [],
        "user_input": user_input,
        "intent": "",
        "tools_needed": [],
        "gathered_data": {},
        "gather_iterations": 0,
        "proposal": None,
        "needs_more_data": False,
        "user_confirmed": None,
        "pending_changes": [],
    }

    # stream until interrupt or completion
    final_state: dict = {}
    async for event in graph.astream(initial_state, config=config):
        final_state = event

    # After streaming, check current snapshot
    snapshot = graph.get_state(config)
    current = snapshot.values if snapshot else {}

    proposal = current.get("proposal")
    pending_tasks = snapshot.next if snapshot else []

    if pending_tasks and "human_review" in pending_tasks:
        return {
            "thread_id": thread_id,
            "proposal": proposal,
            "response": None,
        }

    # Graph ran to completion (no changes needed or already done)
    messages = current.get("messages", [])
    last_ai = next(
        (m.content for m in reversed(messages) if isinstance(m, AIMessage)),
        "好的，我已处理完您的请求。",
    )
    return {
        "thread_id": thread_id,
        "proposal": None,
        "response": last_ai,
    }


async def confirm_proposal(thread_id: str, confirmed: bool) -> dict:
    """Resume the graph after user confirms or rejects the proposal.

    Returns:
        {
            "thread_id": str,
            "status": "applied" | "cancelled",
            "changes_applied": int | None,
        }
    """
    graph = _get_graph()
    config = {"configurable": {"thread_id": thread_id}}

    # With interrupt_before=["human_review"], resume requires:
    # 1. update_state to inject the user's decision into the paused state
    # 2. astream(None, config) to continue execution from the interrupt point
    await graph.aupdate_state(config, {"user_confirmed": confirmed}, as_node="human_review")

    final_state: dict = {}
    async for event in graph.astream(None, config=config):
        final_state = event

    snapshot = graph.get_state(config)
    current = snapshot.values if snapshot else {}
    pending = current.get("pending_changes", [])

    if confirmed:
        return {
            "thread_id": thread_id,
            "status": "applied",
            "changes_applied": len(pending),
        }
    return {
        "thread_id": thread_id,
        "status": "cancelled",
        "changes_applied": None,
    }
